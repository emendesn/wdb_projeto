-- Seleciona Lancamentos SF1
SELECT F1_DTLANC,* from SF1010
WHERE F1_DTLANC BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '
ORDER BY F1_DTLANC

-- Seleciona contabilização CP
SELECT E2_LA,E2_EMIS1,* FROM SE2010 WHERE E2_FILIAL = '01'
AND E2_EMIS1 BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '
AND E2_TIPO NOT IN ('TX','INS')
AND E2_LA = 'S'
AND SUBSTRING(E2_ORIGEM,1,4) = 'FINA' 

-- Seleciona contabilização CR
SELECT E1_LA, E1_EMISSAO,* FROM SE1010
WHERE E1_FILIAL = '01' 
AND E1_EMISSAO BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '
AND E1_LA = 'S'
AND SUBSTRING(E1_ORIGEM,1,4) = 'FINA'

-- Seleciona Mov. Bancario
SELECT E5_LA, E5_DATA, * FROM SE5010
WHERE E5_FILIAL = '01' 
AND E5_DATA BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '
AND E5_LA = 'S'
AND E5_TIPODOC NOT IN ('RA','CH')

-- Seleciona Cheques Emitidos
SELECT EF_LA, EF_DATA, * FROM SEF010
WHERE EF_FILIAL = '01' 
AND EF_DATA BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '
AND EF_LA = 'S'
AND EF_IMPRESS <> 'C'
AND EF_TIPO <> 'PA'


-- Estorna Movimentos SF1
UPDATE SF1010 SET F1_DTLANC = ''
WHERE F1_DTLANC BETWEEN '20100101' AND '20100131'
AND D_E_L_E_T_ = ' '

